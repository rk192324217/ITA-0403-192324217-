# Numeric vector
numeric_data <- c(1.1, 2.2, 3.3)

character_data <- c("A", "B", "C")

logical_data <- c(TRUE, FALSE, TRUE)

cat("Numeric Data:\n")
print(numeric_data)
cat("Type:", typeof(numeric_data), "\n")

cat("Character Data:\n")
print(character_data)
cat("Type:", typeof(character_data), "\n")

cat("Logical Data:\n")
print(logical_data)
cat("Type:", typeof(logical_data), "\n")
